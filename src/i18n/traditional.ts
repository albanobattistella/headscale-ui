export function toTraditionalChinese(value: string) {
  let converted = value;
  for (const [from, to] of phraseMap) {
    converted = converted.replaceAll(from, to);
  }
  return Array.from(converted)
    .map((character) => characterMap[character] ?? character)
    .join("");
}

export function toTraditionalChineseValue<T>(value: T): T {
  if (typeof value === "string") {
    return toTraditionalChinese(value) as T;
  }

  if (Array.isArray(value)) {
    return value.map((item) => toTraditionalChineseValue(item)) as T;
  }

  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value).map(([key, item]) => [key, toTraditionalChineseValue(item)]),
    ) as T;
  }

  return value;
}
